#include <stdio.h>

int main() {
    // 1. 定义原来的数组 a (2行 3列)
    int a[2][3] = {
        {1, 2, 3},
        {4, 5, 6}
    };
    
    // 2. 定义目标数组 b (3行 2列)
    // 必须先把这个架子搭好，才能往里填东西
    int b[3][2];

    // 3. 用双重循环搬运数据
    // 外层遍历 a 的行 (i 从 0 到 1)
    for(int i = 0; i < 2; i++) {
        // 内层遍历 a 的列 (j 从 0 到 2)
        for(int j = 0; j < 3; j++) {
            
            // 核心魔法：把 a 的 (i, j) 搬到 b 的 (j, i)
            b[j][i] = a[i][j];
            
        }
    }

    // 4. 打印验证一下 b (注意 b 是 3行2列)
    printf("转置后的矩阵 b 是:\n");
    for(int i = 0; i < 3; i++) {      // b 只有 3 行
        for(int j = 0; j < 2; j++) {  // b 只有 2 列
            printf("%d ", b[i][j]);
        }
        printf("\n");
    }

    return 0;
}
